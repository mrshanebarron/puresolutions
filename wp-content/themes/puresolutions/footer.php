<script>
document.addEventListener('DOMContentLoaded', function() {
    document.body.classList.add('gsap-ready');

    gsap.registerPlugin(ScrollTrigger);

    // Reveal animations for elements with .gsap-reveal
    document.querySelectorAll('.gsap-reveal').forEach(function(el) {
        var rect = el.getBoundingClientRect();
        if (rect.top < window.innerHeight) {
            // Already in viewport — animate immediately
            gsap.fromTo(el,
                { autoAlpha: 0, y: 30 },
                { autoAlpha: 1, y: 0, duration: 0.8, ease: 'power2.out' }
            );
        } else {
            // Below fold — use ScrollTrigger
            gsap.fromTo(el,
                { autoAlpha: 0, y: 40 },
                {
                    autoAlpha: 1, y: 0, duration: 0.8, ease: 'power2.out',
                    scrollTrigger: {
                        trigger: el,
                        start: 'top 85%',
                        toggleActions: 'play none none none',
                    }
                }
            );
        }
    });

    // Staggered reveals for .gsap-stagger containers
    document.querySelectorAll('.gsap-stagger').forEach(function(container) {
        var children = container.querySelectorAll('.gsap-stagger-item');
        var rect = container.getBoundingClientRect();
        if (rect.top < window.innerHeight) {
            gsap.fromTo(children,
                { autoAlpha: 0, y: 30 },
                { autoAlpha: 1, y: 0, duration: 0.6, stagger: 0.15, ease: 'power2.out' }
            );
        } else {
            gsap.fromTo(children,
                { autoAlpha: 0, y: 30 },
                {
                    autoAlpha: 1, y: 0, duration: 0.6, stagger: 0.15, ease: 'power2.out',
                    scrollTrigger: {
                        trigger: container,
                        start: 'top 85%',
                        toggleActions: 'play none none none',
                    }
                }
            );
        }
    });

    // Stats counter animation
    document.querySelectorAll('[data-count]').forEach(function(counter) {
        var target = parseInt(counter.getAttribute('data-count'));
        var suffix = counter.getAttribute('data-suffix') || '';
        var prefix = counter.getAttribute('data-prefix') || '';

        ScrollTrigger.create({
            trigger: counter,
            start: 'top 90%',
            once: true,
            onEnter: function() {
                var obj = { val: 0 };
                gsap.to(obj, {
                    val: target,
                    duration: 2,
                    ease: 'power2.out',
                    onUpdate: function() {
                        counter.textContent = prefix + Math.floor(obj.val).toLocaleString() + suffix;
                    }
                });
            }
        });
    });
});
</script>

